#include "game.h"

int			rand_num(void)
{
	int		i;
	int		res;

	i = 0;
	srand(time(NULL));
	while (i < 2)
	{
		i++;
		res = rand();
		if ((res % 10) == 0)
			res = 4;
		else
			res = 2;
	}
	return (res);
}
